export function SidebarNav({ sections, licensedFeatures }: { sections: NavSection[]; licensedFeatures?: Feature[] }) {
  const pathname = usePathname();
  const { collapsed } = useSidebar();

  return (
    <nav className="flex flex-1 flex-col gap-1 overflow-y-auto px-3 py-4">
      {sections.map((section) => {
        const visibleLinks = licensedFeatures
          ? section.links.filter((l) => !l.feature || licensedFeatures.includes(l.feature))
          : section.links;

        if (visibleLinks.length === 0) return null;

        return (
          <div key={section.title} className="mb-2">
            {!collapsed && (
              <p className="mb-1 px-3 text-[10px] font-semibold uppercase tracking-wider text-gray-500">
                {section.title}
              </p>
            )}
            {collapsed && <hr className="my-1 border-gray-700" />}
            {visibleLinks.map(({ href, label, icon: Icon }) => {
              const active = href === '/' ? pathname === '/' : pathname.startsWith(href);
              return (
                <Link
                  key={href}
                  href={href}
                  aria-label={`Navigate to ${label}`}
                  title={collapsed ? label : undefined}
                  className={`flex items-center gap-3 rounded-md px-3 py-2 text-sm font-medium transition-colors ${
                    active
                      ? 'bg-gray-700 text-white'
                      : 'text-gray-300 hover:bg-gray-700 hover:text-white'
                  } ${collapsed ? 'justify-center' : ''}`}
                >
                  <Icon className="h-4 w-4 shrink-0" />
                  {!collapsed && <span>{label}</span>}
                </Link>
              );
            })}
          </div>
        );
      })}
    </nav>
  );
}
